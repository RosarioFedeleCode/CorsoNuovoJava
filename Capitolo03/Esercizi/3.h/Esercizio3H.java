public class Esercizio3H {
    public String Break,
    String,      // Bad Practice in quanto String è il nome della classe String, però non essendo parola chiave può essere utilizzata
    character,   // character non è una parola chiave, char invece è una parola chiave
    bit,         // bit non è una parola chiave, byte invece è una parola chiave
    continues,   // continues non è una parola chiave, continue invece è una parola chiave
    exports,     // exports è una parola ad utilizzo limitato e sarebbe inutilizzabile all'interno della dichiarazione di un modulo
    Class,       // Class non è una parola chiave, class invece è una parola chiave
    imports,     // imports non è una parola chiave, import invece è una parola chiave
    _AAA_;
     // _@_,     // Non è legale in quanto non si puà utilizzare @ negli identificatori
     // _;       // É una parola riservata a partire da Java 9
}
